/*calculeting area of a circle*/
#include <iostream>
#include <cmath>
using namespace std;

int main()
{

    double radius = 7;
    const double pi = 3.14159;

    cout << "radius = " << radius << endl;
    cout << "pi = " << pi << endl;
    cout << "area = " << pi * radius * radius << endl;

    return 0;
}