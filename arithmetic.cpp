/*performing arithmetic operations*/

#include <iostream>
using namespace std;

int main()
{
    int a = 8;
    int b = 4;

    cout << "sum = " << (a + b) << endl;
    cout << "difference = " << (a - b) << endl;
    cout << "product = " << (a * b) << endl;
    cout << "quotient = " << (a / b) << endl;
    
    return 0;
}
